#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p_s1;
	size_t	i;

	i = 0;
	p_s1 = (char *)malloc(ft_strlen(s1) + 1);
	if (!p_s1)
		return (NULL);
	while (s1[i] != '\0')
	{
		p_s1[i] = s1[i];
		i++;
	}
	p_s1[i] = '\0';
	return (p_s1);
}
